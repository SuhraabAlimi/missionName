func calculateFactorial(from n: Int) -> Int
{
    print("Calculating factorial(\(n))...")

    if (n <= 1)
    {
        print("Terminal case, returning \(n).")
        return 1
    }
    else
    {
        let nextFactorial = n - 1
        let builtCalculation = n * calculateFactorial(from: nextFactorial)
        print("Non-terminal case, returning \(builtCalculation).")
        return builtCalculation
    }
}
func main()
{
    print("The final result is: \(calculateFactorial(from: n)).")
}
main()
